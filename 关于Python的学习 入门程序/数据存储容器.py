#列表(list)是数据容器的一种，是一次性可以储存多个数据(元素)的
#定义:列表名称 = [元素1,元素2,元素3,元素4,元素5...]  s=[,66,77,88,99,111]
#特点:
    #可以储存2不同类型的元素
    #元素有序，可以重复，元素可以修改(在项目当中，尽量存储同一类型的元素)
    #会给每一个元素定义一个下标，从0开始到n-1结束 这点可以参考数组
    #想获取第一个元素就输入s[0], 第六个元素就就输入s[5]
    #还有反向索引 从-1开始到-n结束，如想获取第一个元素就输入s[-n]，第六个元素就输入s[-n+6]
#想要输出全部索引只需要(定义一个列表s)   print(s)即可
# 要想删除有一个元素 需要用到del(单词删除的简写)   如del s[6](列表中第7个元素)

#列表的切片:切片是指对操作的数据截取其中一部分的操作  列表，字符串，元组都支持切片操作
    #语法:序列数据[开始索引:结束索引:步长]
    #不包括结束索引位置对应的元素(开始索引未指定时则默认0;结束索引未指定时则默认为列表长度(直到列表尾部);步长未指定则默认为1) 但是并不会包含结束索引
    #索引采用正向,反向索引都可以
    #步长是选取间隔，默认步长为1

#列表的方法(方法就像是物品具有的能力或功能 如空调的能力为制热 制冷 通风 把这些功能都称为方法 而空调这个物品则称为对象 方法归于对象)
#在Python中一切皆为对象
    #列表的常用方法就是指列表这种数据类型内置的常见功能(添加元素，删除元素，排序等)
    #首先定义一个列表s  一般格式为 对象.方法名
    #append()  在列表尾部追加元素                                         s.append(10088)
