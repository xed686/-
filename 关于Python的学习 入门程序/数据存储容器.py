    #remove()  移除列表中第一个匹配到的值(是值而并非索引)                      s.remove(75)
    #pop()     删除列表中指定索引位置的元素且会返回(如果为指定索引,默认删最后一个)  s.pop()
    #sort()    对列表进行排序(列表元素的数据类型一致，才可以进行排序)            s.sort()
    #reverse() 反转列表元素                                                s.reverse()
    #count()   查看列表中的一个元素出现的次数                                  s.sount()
