# 关于元组(tuple)
#为什么不用列表而还要再去学习用元组？ 列表的特点为:元素可重复，有序，可以修改，但如果要记录一些信息而这些信息2不能被修改，只能查询呢 此时列表就不合适了
#元组与列表最大的不同点在于:元组一旦定义完成，不可被修改
#介绍:元组是不可变的序列，类似于列表，但创建后不能被修改
#特点:可以储存不同的元素  元素可以重复有序，不可以被修改(支持索引访问，切片)

# 元组的定义:元组名称 = (元素1,元素2,元素3...)
# 定义空元组: 方法1 元组名称 = ()
#           方法2 元组名称 = tuple()

# 元组的方法
# count()  统计某元素在元组中出现的次数
# index()  查找某个元素在元组中出现的索引位置(如果有两个及以上的重复元素，查找的是第一次出现的位置)
# 也支持切片 如定义了一个元组 s = (1,2,3)  print(s[0;1;2])


# 元组的组包与解包
#定义元组，组包
t1 = ( 5,6,7,9)
#定义元组，组包
t2 = 5,7,9,1#不加括号也可以 运行的时候编译器会认为这是一个元组 会自动加上括号
#基础解包
a,b,c,d = t1
#(*)扩展解包
x,*y,z = t2#x为5，y为[7，9] z为1
s,*o=t2#s为5，y为[7,9,1]
# 在元组解包时 *表示收集剩余所有元素，允许我们处理不确定数量的元素(生成列表，以便我们进行进一步的探讨)


