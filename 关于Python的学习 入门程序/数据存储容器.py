# 字符串
# 介绍:字符串是字符的容器，一个字符串中可以存放任意数量的字符 如"Python" 'Python' """Python"""
# 特点: 不可变性(无法修改)，有序性，可迭代性(能通过for循环一遍一遍的遍历出来)
# 字符串中的每一个字符元素都有对应的下标(索引 与序列一样)，通过元素对应的索引，就可以获取对应的元素

# 语法： 序列对象[开始索引:结束索引:步长]
#     不包括结束索引位置对应的元素(开始索引未指定默认为0；结束索引未指定默认为1；步长未2指定默认为1)
#     索引采用正向，反向索引都可以
#     步长是选取间隔，默认步长为1(如果开始索引是-1，表示从后向前)
#字符串的常用方法(字符串本身并不会被改变，方法中会改变字符串的都是返回改变后的结果 但是字符长本身是并没有被改变的 可以赋一个新的值来储存返回的值)
    # find()   在字符串中查找子串(字符串的一部分)，返回第一次出现的索引位置，找不到返回-1   s.find('Python')
    # count()  统计字串在字符串中出现的次数                                          s.count('P')
    # upper()  将字符串中的所有字母转换为大写(字符串本身并不会改变而是返回这个结果)          s.upper()
    # lower()  将字符串中所有字母转换为小写(原理如上)                                  s.lower()
    # split()  将字符串按照指定分隔符分割成列表                                        s.split(' ')
    # strip()  去除字符串两端的空白字符或指定字符                                       s.strip()/s.strip('*')
    # replace()将字符串中的指定子串替换成新的字串                                      s.replace('H','C')
    # starswith()检查2字符串是否已指定字串开头，返回布尔值                               s,starswith('P')
