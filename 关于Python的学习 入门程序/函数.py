#函数的嵌套调用
# 嵌套调用指的是在一个函数中，又调用了另外的函数
#函数调用遵循栈结构，最后被调用的函数最先返回LIFO(Last In First Out  先进后出 后进先出 如同压子弹一样 最先压进去的最后出 最后压进去的最先出)

#定义一个函数 根据传入的秒数来输出 小时:分钟:秒
def Time(Second):
    if Second >= 3600:
        Hour = round(Second / 3600,0)
        Minute = round((Second - Hour * 3600) /60,0)
        second = Second - (Hour * 3600 + Minute * 60)
    elif Second >= 60 and Second < 3600:
        Hour = 0
        Minute = round(Second / 60,0)
        second = Second - (Minute * 60)
    else:
        Hour = 0
        Minute = 0
        second = Second
    return Hour, Minute, second
time = int(input("请输入你要计算的秒数"))
h,m,s = Time(time)
print(h,m,s)

