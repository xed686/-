#匿名函数
# 匿名函数指的是没有名称的函数，需要通过lambda表达式来声明函数，可以简化简单的函数(单行表达式) 想要调用的时候可以用一个变量来接受赋值 让这个变量变成一个函数
#匿名函数的定义
# lambda 参数列表 : 函数体   #不能换行
# lambda : print("--------------------")
# lambda x,y: x+y
#函数逻辑比较简单(单行表达式)且只在一个地方使用时，可以考虑使用匿名函数来简化书写(通常作为高阶函数的参数来使用)
#匿名函数中可以返回结果，也可以不返回结果。返回结果时，不需要写return，表达式的运行结果就是要返回的值

#类型注解是Python中的一种语法特性，用于明确标识变量，函数参数和返回值的数据类型，从而使代码更清晰，更安全，更易维护
# 法1   :变量名: 容器名[变量类型] = 输入的元素
# 法2   :变量名: 变量类型 = 输入的元素
#类型注解只是起到了语法提示作用，并不会影响程序运行的结果
#为函数添加类型注解，其实主要就是为函数的参数和返回值添加类型注解，具体语法如下:
def calc(scores:list[int]) ->float:# 这里的 ->类型名 指的是为函数的返回值添加类型注解
    return sum(scores) / len(scores)