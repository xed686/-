#函数的传参方式指的是，再调用函数时，传递实参的方式具体有两种方式
#1.位置参数:调用函数时根据函数定义时的位置来传递参数 优点:简洁  缺点:可读性强，维护难，易出错
#2.关键字传参:调用函数时以函数定义时形参名称作关键字，以'键(内部参数名)=值(全局参数名)'的形式来传递参数(不要求顺序) 可以两种方法混用 不过需要先写位置参数 后写关键字参数  优缺点与法1相反

#默认参数也称缺省参数，用于在定义函数时，为参数提供默认值，调用函数时，可以不传递有默认值的参数 如下:
def reg_stu(name,age,gender,city = '北京'):
    print(f"注册成功，姓名:{name},年龄:{age},性别:{gender}，城市:{city}")
    return{"name":name,"age":age,"gender":gender,"city":city}
sut = reg_stu('张山',18,'男')
print(sut)
sut = reg_stu('张山',18,'男','郴州')
print(sut)
#注意:默认参数必须放在没有默认值的参数列表的后面，一个函数时可以定义多个默认参数的
#注意:调用函数时，如果为默认参数传递了值，则会修改默认2的参数值;如果没有传递该参数，则直接使用默认值

# 不定长参数 再需要定义的参数中如果有不确定的参数数量时需要用到
#简介:不定长参数也叫可变参数，用于函数定义及调用时参数个数不确定(0个或多个)的场景
# 类型:
#     位置传递
#     关键字传递
#位置传递:
def calc_data(*args):
    a = sum(args)
    return a
#调用函数
data = calc_data(1,2,2,3,3,43,54,3,3,45,2,5,3)
print(data)
#注意:传递的所有匹配的位置都会被args变量收集，这些参数会合并封装为一个元组 args是元组类型(并不会封装关键字参数) args只是约定俗成的变量名，并不是关键字，这里可以使用任何合法的变量名(如*data)

#关键字传递的不定长参数(**参数名)约定俗成的名字就是kwargs(单词关键字的缩写)
def calc_data(*args,**kwargs):
    a = sum(args)
    return a
#调用函数
data = calc_data(1,2,2,3,3,43,54,3,3,45,2,5,3,round = 1,print = True)#参数是以"键=值"形式传递的关键字参数，这些"键=值"参数都会被kwargs接受，并合并封装为一个字典类型  kwargs只是约定俗成的变量名，并不是关键字，这里可以使用任何合法的变量名(如**data)
#其中 round = 1 表示结果要保留一位小数 print = True 表示将返回值输出出来
print(data)

